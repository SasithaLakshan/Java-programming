public class characters {
    public static void main(String[] args){
       
       char [] FirstName = {'S','A','S','I','T','H','A'};
       char [] MiddleName = {'L','A','K','S','H','A','N'};

       for(char c:FirstName){
           System.out.println(c);
       }
       for(char d:MiddleName){
           System.out.println(d);
       }
    }
}
